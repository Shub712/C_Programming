///////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[20] = {'\0'};

    printf("Enter The String : \n");
    scanf("%[^ '\n']s", Arr);

    StrlwrX(Arr);

    printf("The Updated String is : %s", Arr);

    return 0;

}

