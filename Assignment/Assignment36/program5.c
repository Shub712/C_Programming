int CountSpace(char *str)
{
    int iCount = 0;

    while(*str != '\0')
    {
        if(*str == ' ')
        {
            iCount++;
        }
    
        str++;
    }
    
    return iCount;
}

////////////////////////////////////////////////////////////////////////
//
//  Enrty point function of a application
//
///////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[20] = {'\0'};
    int iRet = 0;

    printf("Enter The String : \n");
    scanf("%[^'\n']s", Arr);

    iRet = CountSpace(Arr);

    printf("Spaces are : %d", iRet);

    return 0;

}

