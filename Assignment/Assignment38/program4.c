void StrCpySmall(char *str, char *dest)   
{   
 

    while(*str != '\0')
    {
        if(*str >= 'A' && *str<= 'Z')
        {
            *dest = (*str + 32);
        }
        else
        {
            *dest = *str;
        }
        dest++;
        str++;
    }
}

////////////////////////////////////////////////////////////////////////
//
//  Enrty point function of a application
//
///////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[30] = "Marvellous Python 2";
    char Brr[30] = {'\0'};

    StrCpySmall(Arr, Brr);
    printf("%s\n", Brr);  

    return 0;
}

