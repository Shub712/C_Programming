void StrCpyRev(char *src, char *dest)
{
    char *start =  src;
    char *end =  src;

    char *temp = '\0';

    while(*end != '\0')
    {
        end ++;
    }

    end--;

    while(start <= end)
    {
        temp = *src;
        *dest = *end;
        *end = temp;

        dest++;
        end --;
    }

}

////////////////////////////////////////////////////////////////////////
//
//  Enrty point function of a application
//
///////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[30] = "Marvellous Python";
    char Brr[30] = {'\0'};

    StrCpyRev(Arr,Brr);
    printf("%s\n", Brr);  

    return 0;
}

