#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////
//
//  Function Name = Number
//  Description = It is use to display smalll and large according to the number
//  Input =  Integer
//  Output = String
//  Author = Shubham Kiran Pawar
//  Date = 28/10/2025
///////////////////////////////////////////////////////////////////////////////

void Number(int iNo)
{
    if (iNo<100)
    {
        printf("Small\n");
    }
    else                                                                   // Business Logic
    {
        printf("Large\n");
    }
    
}

// Time Complexity = O(N)

/////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of a application
//
////////////////////////////////////////////////////////////////////////////

int main()
{   
    int iValue = 0;

    printf("Enter The Number :\n");
    scanf("%d", &iValue);

    Number(iValue);

    return 0;
}

