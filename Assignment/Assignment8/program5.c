///////////////////////////////////////////////////////////////////////////////
//
//  Function Name = Table
//  Description = It is use to print reverse table of number
//  Input =  Integer
//  Output = Integer
//  Author = Shubham Kiran Pawar
//  Date = 28/10/2025
//
///////////////////////////////////////////////////////////////////////////////

void Table(int iNo)
{
    int iCnt = 0;

    if(iNo<0)
    {
        iNo = -iNo;
    }

    for(iCnt = 10; iCnt>=1; iCnt--)                                         // Business logic
    {
        printf("%d\t", iCnt*iNo);
    }
}

//Time Complexity = O(N) 

/////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of a application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    printf("Enter the number :\n");
    scanf("%d", &iValue);

    Table(iValue);
    return 0;
}

