void Table(int iNo)
{
    int iCnt = 0;

    if(iNo<0)
    {
        iNo = -iNo;
    }

    for(iCnt = 1; iCnt<=10; iCnt++)                                         // Business logic
    {
        printf("%d\t", iNo*iCnt);
    }
}

//Time Complexity = O(N) 

/////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of a application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    printf("Enter the number :\n");
    scanf("%d", &iValue);

    Table(iValue);
    return 0;
}

