#include<stdio.h>

////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description :   Used to give decimal, hexadecimal, octal value of a character
//  Input   :       character
//  Output  :       Integer(To display on console)
//  Author  :       Shubham Kiran Pawar
//  Date    :       29/11/2025
//
///////////////////////////////////////////////////////////////////////

void Display(char ch)
{
    if(ch>='A'&& ch<='Z')
    {
        printf("DEC: %d Hex: %x Oct : OX%o\n", ch,ch,ch);
    }
}

////////////////////////////////////////////////////////////////////////
//
//  Enrty point function of a application
//
////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';

    printf("Enter The character : \n");
    scanf("%c", &cValue);

    Display(cValue);

    return 0;
}

