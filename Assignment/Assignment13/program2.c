///////////////////////////////////////////////////////////////////////////////
//
//  Function Name = print_even_numbers
//  Description = It is use to display the even numbers
//  Input =   Integer
//  Output =  Integer
//  Author =  Shubham Kiran Pawar
//  Date =    30/10/2025
//
///////////////////////////////////////////////////////////////////////////////

void print_even_numbers(int Limit)
{
    int iCnt = 0;

    for(iCnt=1; iCnt<=Limit; iCnt++)                           // Business Logic
    {
        if(iCnt % 2 == 0)
        {
            printf("%d\t", iCnt);
        }
    }
}

// Time Complexity = O(n)

/////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of a application
//
////////////////////////////////////////////////////////////////////////////


int main()
{
    int Limit = 0;
    printf("Enter Number : \n");
    scanf("%d", &Limit);
    
    print_even_numbers(Limit);

    return 0;
}

