#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////
//
//  Function Name = sum_even_numbers
//  Description = It is use to display the sum of even numbers
//  Input =   Integer
//  Output =  Integer
//  Author =  Shubham Kiran Pawar
//  Date =    30/10/2025
//
///////////////////////////////////////////////////////////////////////////////

int sum_even_numbers(int Limit)
{
    int iCnt = 0;
    int iSum = 0;

    for(iCnt=1; iCnt<=Limit; iCnt++)                           // Business Logic
    {
        if(iCnt%2==0)
        {
            iSum = iSum + iCnt;
        }
    }

    return iSum;
}

// Time Complexity = O(n)

/////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of a application
//
////////////////////////////////////////////////////////////////////////////


int main()
{
    int Limit = 0;
    printf("Enter Number : \n");
    scanf("%d", &Limit);
    
    printf("%d\n", sum_even_numbers(Limit) );

    return 0;
}

