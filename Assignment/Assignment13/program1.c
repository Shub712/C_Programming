#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////
//
//  Function Name = print_numbers
//  Description = It is use to display the numbers
//  Input =   Integer
//  Output =  Integer
//  Author = Shubham Kiran Pawar
//  Date = 30/10/2025
//
///////////////////////////////////////////////////////////////////////////////

void print_numbers(int Limit)
{
    int iCnt = 0;

    for(iCnt=1; iCnt<=Limit; iCnt++)                                        // Business Logic
    {
        printf("%d\t", iCnt);
    }
}

// Time Complexity = O(n)


/////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of a application
//
////////////////////////////////////////////////////////////////////////////


int main()
{
    int Limit = 0;
    printf("Enter Number : \n");
    scanf("%d", &Limit);
    
    print_numbers(Limit);

    return 0;
}

