int SumOfFactors(int iNo)
{
    int iSum = 0;
    int iCnt = 0;

    for(iCnt=1; iCnt<=iNo; iCnt++)
    {
        if(iNo % iCnt == 0)
        {
            iSum = iSum + iCnt;                                             // Business Logic
        }
    }
    return iSum;
}

// Time Complexity - O(n)

/////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of a application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;

    printf("Enter Number :\n");
    scanf("%d", &iValue);

    iRet = SumOfFactors(iValue);

    printf("The Sum of Factors is : %d \n", iRet);

    return 0;
}

