///////////////////////////////////////////////////////////////////////////////
//
//  Function Name = SumOfFactors
//  Description = It is use to display the sum of the factors.
//  Input =   Integer
//  Output =  Integer
//  Author = Shubham Kiran Pawar
//  Date = 29/10/2025
//
