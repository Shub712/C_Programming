///////////////////////////////////////////////////////////////////////////////
//
//  Function Name = Print_Factors
//  Description = It is use to display the factors of a number
//  Input =  Integer
//  Output =  Integer
//  Author = Shubham Kiran Pawar
//  Date = 29/10/2025
//
