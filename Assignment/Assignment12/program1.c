void Print_Factors(int iNo)
{
    int iCnt = 0;

    for(iCnt=1; iCnt<=iNo; iCnt++)
    {
        if(iNo % iCnt == 0)                                                  // Business Logic
        {
            printf("%d\t", iCnt);
        }
    }
}

// Time Complexity - O(n)

/////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of a application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter Number: \n");
    scanf("%d", &iValue);

    Print_Factors(iValue);

    return 0;
}

