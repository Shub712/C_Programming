double SquareMeter(int iNo)
{
    double dAns = 0.0;

    dAns = (iNo * 0.0929);                                                  // Business Logic

    return dAns;
}

/////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of a application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    double dRet = 0.0;

    printf("Enter area in square feet: \n");
    scanf("%d", &iValue);

    dRet = SquareMeter(iValue);

    printf("The Square meter is : %lf", dRet);

    return 0;
}

