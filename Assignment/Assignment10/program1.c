double CircleArea(float fRadius)
{
    double dAns = 0.0;
    float fPi = 0.0f;

    fPi = 3.14;

    dAns = fPi * fRadius * fRadius;                                         // Business Logic

    return dAns;
}

//  Time Complexity - O(N)

/////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of a application
//
////////////////////////////////////////////////////////////////////////////


int main()
{
    float fValue = 0.0f;
    double dRet = 0.0;

    printf("Enter Radius : \n");
    scanf("%f", &fValue);

    dRet = CircleArea(fValue);

    printf("The Area of circle is : %lf", dRet);


    return 0;
}

