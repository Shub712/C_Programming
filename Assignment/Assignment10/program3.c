////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter a distance : \n");
    scanf("%d", &iValue);

    iRet = KMToMeter(iValue);
    printf("The Coversation is : %d Meter",iRet);


    return 0;
}

