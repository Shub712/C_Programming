double FhToCs(float fTemp)
{
    double dAns = 0.0;
    dAns = (fTemp - 32) * 5/9 ;                                             // Business Logic

    return dAns;
}

/////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of a application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0.0f;
    double dRet  = 0.0;

    printf("Enter Temperature in farenheit: \n");
    scanf("%f", &fValue);

    dRet = FhToCs(fValue);

    printf("The Temperature in celcius is : %lf", dRet);

    return 0;
}

