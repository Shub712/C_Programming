#include<stdio.h>

//////////////////////////////////////////////////////
//  Function name : Display
//  Description :   used to display the pattern
//  Input :         Integer
//  Ouput :         Pattern
//  Author :        Shubham Kiran Pawar
//  Date :          10/12/2025
//////////////////////////////////////////////////////

void Display(int No)
{
    static int iCnt = 1;
    static char ch = 'a';

    if(iCnt <= No)
    {
        printf("%c\t",ch);
        iCnt++;
        ch++;
        Display(No);
    }
    printf("\n");
}

//////////////////////////////////////////////////////
//
//  Entry point function of a application
//
//////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter The Number : \n");
    scanf("%d", &iValue);
    
    Display(iValue);

    return 0;
}


