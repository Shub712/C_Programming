//////////////////////////////////////////////////////////////
//
//  Function Name = Pattern
//  Description = It is use to print "$ *"
//  Input =  Integer
//  Output = Special Symbols
//  Author = Shubham Kiran Pawar
//  Date = 28/10/2025
