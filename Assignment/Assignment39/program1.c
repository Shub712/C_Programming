#include<stdio.h>

//////////////////////////////////////////////////////
//  Function name : Display
//  Description :   used to display the pattern
//  Input :         Integer
//  Ouput :         Pattern
//  Author :        Shubham Kiran Pawar
//  Date :          08/12/2025
//////////////////////////////////////////////////////

void Display()
{
    static int iCnt = 1;
    
    if(iCnt<=5)
    {
        printf("*\t");
        iCnt++;
        Display();
    }

    printf("\n");
}

//////////////////////////////////////////////////////
//
//  Entry point function of a application
//
//////////////////////////////////////////////////////

int main()
{
    Display();

    return 0 ;
}

