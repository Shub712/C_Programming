//
//  Enrty point function of a application
//
///////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[20]= {'\0'};

    printf("Enter The String : \n");
    scanf("%[^ '\n']s",Arr);

    StrrevX(Arr);

    return 0 ;
}

