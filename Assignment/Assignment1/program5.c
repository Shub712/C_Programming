int Accept(int iNo)
{
    int iCnt = 0;
    for(iCnt = 1; iCnt<=iNo; iCnt++)                        // Business Logic
    {
        printf("%d\t*\t",iCnt);
    }
    printf("\n");
}

//////////////////////////////////////////////////////////////
//
// Entry point function for the application
//
//////////////////////////////////////////////////////////////


int main()
{
    int iValue = 0;
    iValue = 5 ;

    Accept(iValue);


    return 0;
}

