void CheckEvenOdd(int iNo)
{
    if(iNo<0)
    {
        iNo = -iNo;
    }
    
    if((iNo % 2==0))                                                                // Business Logic
    {
        printf("This Number is Even\n");
    }
    else
    {
        printf("This Number is a Odd number\n");
    }
}

////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point of main function
//
///////////////////////////////////////////////////////////////////////////////////////


int main()
{
    int iValue=0;

    printf("Enter The Number : \n");
    scanf("%d", &iValue);

    CheckEvenOdd(iValue);


    return 0;
}

