void CheckNumberType(int iNo)
{
    if(iNo<0)
    {
        printf("It is a negative number\n");
    }
    if(iNo>0)
    {
        printf("It is a positive number\n");                              // Business Logic
    }
    else
    {
        printf("It is a zero number\n");
    }

}

