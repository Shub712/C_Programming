//
//  Entry Point of main function
//
///////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter The Number : \n");
    scanf("%d", &iValue);

    CheckNumberType(iValue);

    return 0;
}

