int FindMax(int iNo1, int iNo2)
{
    if(iNo1 < 0)
    {
        iNo1 = -iNo1;
    }                                                                                   // Business Logic
    if(iNo2 < 0)
    {
        iNo2 = -iNo2;
    }

    if(iNo1 < iNo2)
    {
        return iNo2;
    }
    else
    {
        return iNo1;
    }
}

////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point of main function
//
///////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue1, iValue2 = 0;
    int iRet=0;

    printf("Enter two numbers\n");
    scanf("%d %d", &iValue1, &iValue2);

    iRet = FindMax(iValue1, iValue2);
    printf("The maximum number is: %d", iRet);

    return 0;
}

