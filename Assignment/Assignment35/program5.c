#include<stdio.h>

////////////////////////////////////////////////////////////////////////
//
//  Function Name : DisplayReverse
//  Description :   Used to display string in reverse order
//  Input   :       String
//  Output  :       String(To display on console)
//  Author  :       Shubham Kiran Pawar
//  Date    :      1/12/2025
//
///////////////////////////////////////////////////////////////////////


void DisplayReverse(char *str)
{
    char *temp = str;

    while(*str != '\0')
    {
        str++;
    }

    while(str>=temp)
    {
        printf("%c", *str);
        str--;
    }
    printf("\n");
}

/////////////////////////////////////////////////////////////////////////
//
//  Entry point function of a application
//
/////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[20] = {'\0'};

    printf("Enter The String : \n");
    scanf("%[^'\n']s", Arr);

    DisplayReverse(Arr);

    return 0;
}

