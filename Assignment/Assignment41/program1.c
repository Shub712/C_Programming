#include<stdio.h>

///////////////////////////////////////////////////////////////
//  Function name : Display
//  Description :   used to display the pattern
//  Input :         Integer
//  Ouput :         Pattern
//  Author :        Shubham Kiran Pawar
//  Date :          10/12/2025
///////////////////////////////////////////////////////////////

void Display(int iNo)
{

    if(iNo>=1)
    {
        printf("%d\t*\t",iNo);
        Display(iNo-1);
    }
    printf("\n");
}

///////////////////////////////////////////////////////////////
//
//  Entry point function of a application
//
///////////////////////////////////////////////////////////////

int main()
{
    int iValue =  0;

    printf("Enter The Number :\n");
    scanf("%d", &iValue);

    Display(iValue);

    return 0;
}

