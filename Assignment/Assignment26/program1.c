void Display(int iNo)
{
    int iCnt = 0;
    char ch = 0;

    for(iCnt=0; iCnt<iNo; iCnt++)
    {
        ch =(char)('A'+iCnt);                                       // Business Logic
        printf("%c\t",ch);      
    }
    printf("\n");
}

////////////////////////////////////////////////////////////////////////
//
//  Enrty point function of a application
//
////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter The Number of elements :\n");
    scanf("%d", &iValue);

    Display(iValue);

    return 0;
}

